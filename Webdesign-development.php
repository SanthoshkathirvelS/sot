            accordionItems.forEach((item) => {
                const header = item.querySelector(".accordion-header");
                const content = item.querySelector(".accordion-content");
                const innerAccordion = item.querySelector(".inner-accordion");

                header.addEventListener("click", () => {
                    content.classList.toggle("show");
                    header.classList.toggle("active");

                    // Hide other open content sections
                    accordionItems.forEach((otherItem) => {
                        if (otherItem !== item) {
                            otherItem
                                .querySelector(".accordion-content")
                                .classList.remove("show");
                            otherItem
                                .querySelector(".accordion-header")
                                .classList.remove("active");
                        }
                    });
                });

                innerAccordion.addEventListener("click", (event) => {
                    event.stopPropagation();
                });

                const innerAccordionItems = item.querySelectorAll(
                    ".inner-accordion .accordion-item"
                );

                innerAccordionItems.forEach((innerItem) => {
                    const innerHeader = innerItem.querySelector(".accordion-header");
                    const innerContent = innerItem.querySelector(".accordion-content");

                    innerHeader.addEventListener("click", () => {
                        innerContent.classList.toggle("show");
                        innerHeader.classList.toggle("active");

                        // Hide other open inner content sections
                        innerAccordionItems.forEach((otherInnerItem) => {
                            if (otherInnerItem !== innerItem) {
                                otherInnerItem
                                    .querySelector(".accordion-content")
                                    .classList.remove("show");
                                otherInnerItem
                                    .querySelector(".accordion-header")
                                    .classList.remove("active");
                            }
                        });
                    });
                });
            });
        });
    </script>
